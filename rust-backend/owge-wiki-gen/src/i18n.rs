//! UI translations. Only the wiki chrome is translated (labels, headings,
//! requirement phrasing) — entity names/descriptions come straight from the
//! universe database and stay in whatever language the admin wrote them
//! (same scope as the game frontend's i18n).
//!
//! Spanish terms deliberately match the game frontend's translation files
//! (`game-frontend/projects/*/src/assets/i18n/es.json`): Mejoras, Especiales
//! de tiempo, Tipos de unidad, Grupos de velocidad, Atributos, Atraviesa
//! escudos, Sin asignar, …

/// One language: its BCP-47 code and the full string table.
pub struct LangDef {
    pub code: &'static str,
    /// Label shown on the switcher link *to* this language.
    pub label: &'static str,
    pub tr: &'static Tr,
}

